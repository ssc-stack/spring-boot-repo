public class Mario {
    public void up() {
        System.out.println("Jump");
    }

    public void down() {
        System.out.println("Slide");
    }

    public void left() {
        System.out.println("Move Back");
    }

    public void right() {
        System.out.println("Accelerate");
    }
}
